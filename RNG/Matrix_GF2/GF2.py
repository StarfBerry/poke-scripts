def gf2_to_int(bits):
    return sum(bits[i] << i for i in range(len(bits)))

def gf2_to_ints(bits, size=64):
    return [gf2_to_int(bits[i*size:(i+1)*size]) for i in range(len(bits) // size)]

def gf2_mul_mod(a, b, m, degre):
    res = 0

    while a:
        if a & 1:
            res ^= b
        
        a >>= 1
        b <<= 1
        
        if b >> degre:
            b ^= m
    
    return res

def gf2_pow_mod(base, exp, mod, degre=128):
    res = 1

    while exp:
        if exp & 1:
            res = gf2_mul_mod(res, base, mod, degre)
        
        base = gf2_mul_mod(base, base, mod, degre) # precompute 2^exp mod charpoly ?
        exp >>= 1
    
    return res

